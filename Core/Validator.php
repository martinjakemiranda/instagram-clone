<?php

namespace Core;
//Pure function that is not contingent or dependent upon state or values from the outside world
//It's not dependent upon existing internal

class Validator
{
    public static function string($value, $min = 1, $max = INF)
    {
        $value = trim($value);

        return strlen($value) >= $min && strlen($value) <= $max;
    }

    public static function email($value)
    {
        return filter_var($value, FILTER_VALIDATE_EMAIL);
    }

}